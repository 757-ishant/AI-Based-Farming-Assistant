const __i18nWarned = new Set();
const __i18nTextToKey = new Map();
let __i18nObserver = null;
let __i18nApplying = false;
let __i18nScheduled = false;

function __normText(v) {
  return String(v || "")
    .replace(/\s+/g, " ")
    .trim();
}

function __buildReverseLookup() {
  const packs = Object.keys(TRANSLATIONS || {});
  packs.forEach((langCode) => {
    const pack = TRANSLATIONS[langCode] || {};
    Object.keys(pack).forEach((k) => {
      const raw = pack[k];
      if (raw == null || raw === "") return;
      const n = __normText(raw);
      if (!n) return;
      if (!__i18nTextToKey.has(n)) __i18nTextToKey.set(n, k);
    });
  });
}

function __warnOnce(id, message) {
  if (__i18nWarned.has(id)) return;
  __i18nWarned.add(id);
  console.warn(message);
}

function __getLang() {
  return localStorage.getItem("selectedLanguage") || "hi";
}

function t(key) {
  const lang = __getLang();
  const localized = TRANSLATIONS[lang]?.[key];
  if (localized !== undefined && localized !== "") return localized;
  const en = TRANSLATIONS.en?.[key];
  if (en !== undefined && en !== "") {
    __warnOnce(
      "missing-lang:" + lang + ":" + key,
      `[i18n] Missing key "${key}" for language "${lang}". Falling back to English.`
    );
    return en;
  }
  __warnOnce("missing-en:" + key, `[i18n] Missing translation key "${key}" in all packs.`);
  return key;
}

function __collectNodes(scope, selector) {
  const out = [];
  if (!scope) return out;
  if (scope.nodeType === 1 && scope.matches(selector)) out.push(scope);
  if (scope.querySelectorAll) {
    scope.querySelectorAll(selector).forEach((n) => out.push(n));
  }
  return out;
}

function __inferKeyFromText(text) {
  const n = __normText(text);
  if (!n) return "";
  return __i18nTextToKey.get(n) || "";
}

function __getDirectText(el) {
  if (!el || !el.childNodes) return "";
  let text = "";
  el.childNodes.forEach((n) => {
    if (n.nodeType === Node.TEXT_NODE) text += n.textContent || "";
  });
  return __normText(text);
}

function __autoBindI18nAttrs(scope) {
  const root = scope || document;
  const nodes = __collectNodes(root, "*");
  nodes.forEach((el) => {
    const tag = (el.tagName || "").toLowerCase();
    if (!tag || tag === "script" || tag === "style" || tag === "noscript") return;

    if (!el.hasAttribute("data-i18n")) {
      const directText = __getDirectText(el);
      const key = __inferKeyFromText(directText);
      if (key) el.setAttribute("data-i18n", key);
    }

    if (!el.hasAttribute("data-i18n-placeholder") && "placeholder" in el && el.placeholder) {
      const key = __inferKeyFromText(el.placeholder);
      if (key) el.setAttribute("data-i18n-placeholder", key);
    }

    if (!el.hasAttribute("data-i18n-title") && el.title) {
      const key = __inferKeyFromText(el.title);
      if (key) el.setAttribute("data-i18n-title", key);
    }

    if (!el.hasAttribute("data-i18n-value") && (tag === "input" || tag === "option") && el.value) {
      const key = __inferKeyFromText(el.value);
      if (key) el.setAttribute("data-i18n-value", key);
    }
  });
}

function applyTranslations(root) {
  const scope = root || document;
  __i18nApplying = true;
  try {
    __autoBindI18nAttrs(scope);

    __collectNodes(scope, "[data-i18n]").forEach((node) => {
      const k = node.getAttribute("data-i18n");
      if (!k) return;
      const val = t(k);
      const tag = (node.tagName || "").toLowerCase();
      if (tag === "input" || tag === "textarea") {
        if (node.placeholder !== val) node.placeholder = val;
      } else {
        if (node.textContent !== val) node.textContent = val;
      }
    });

    __collectNodes(scope, "[data-i18n-placeholder]").forEach((node) => {
      const k = node.getAttribute("data-i18n-placeholder");
      if (!k) return;
      const val = t(k);
      if (node.placeholder !== val) node.placeholder = val;
    });

    __collectNodes(scope, "[data-i18n-html]").forEach((node) => {
      const k = node.getAttribute("data-i18n-html");
      if (!k) return;
      const val = t(k);
      if (node.innerHTML !== val) node.innerHTML = val;
    });

    __collectNodes(scope, "[data-i18n-title]").forEach((node) => {
      const k = node.getAttribute("data-i18n-title");
      if (!k) return;
      const val = t(k);
      if (node.title !== val) node.title = val;
    });

    __collectNodes(scope, "[data-i18n-value]").forEach((node) => {
      const k = node.getAttribute("data-i18n-value");
      if (!k) return;
      const val = t(k);
      if (node.value !== val) node.value = val;
    });
  } finally {
    __i18nApplying = false;
  }
}

function __scheduleApply(root) {
  if (__i18nScheduled) return;
  __i18nScheduled = true;
  requestAnimationFrame(() => {
    __i18nScheduled = false;
    applyTranslations(root || document);
  });
}

function __setupMutationObserver() {
  if (__i18nObserver || !document.body) return;
  __i18nObserver = new MutationObserver((mutations) => {
    if (__i18nApplying) return;
    let shouldReapply = false;
    for (const m of mutations) {
      if (m.type === "childList" && (m.addedNodes?.length || m.removedNodes?.length)) {
        shouldReapply = true;
        break;
      }
      if (m.type === "attributes" && m.target) {
        shouldReapply = true;
        break;
      }
      if (m.type === "characterData") {
        shouldReapply = true;
        break;
      }
    }
    if (shouldReapply) __scheduleApply(document);
  });
  __i18nObserver.observe(document.body, {
    subtree: true,
    childList: true,
  });
}

window.switchLanguage = function (code) {
  localStorage.setItem("selectedLanguage", code);
  document.documentElement.lang = code;
  applyTranslations(document);
  document.querySelectorAll(".global-lang-select").forEach((sel) => {
    sel.value = code;
  });
};

function __initLanguageSystem() {
  __buildReverseLookup();
  const currentLang = __getLang();
  document.documentElement.lang = currentLang;

  const selectors = document.querySelectorAll(".global-lang-select");
  selectors.forEach((sel) => {
    sel.innerHTML = "";
    LANGUAGES.forEach((lang) => {
      const opt = document.createElement("option");
      opt.value = lang.code;
      opt.textContent = lang.native;
      if (lang.code === currentLang) opt.selected = true;
      sel.appendChild(opt);
    });
    sel.addEventListener("change", (e) => {
      window.switchLanguage(e.target.value);
    });
  });

  applyTranslations(document);
  __setupMutationObserver();
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", __initLanguageSystem);
} else {
  __initLanguageSystem();
}

window.reapplyTranslations = function () {
  applyTranslations(document);
};

window.LANGUAGES = LANGUAGES;
window.TRANSLATIONS = TRANSLATIONS;
window.t = t;
window.applyTranslations = applyTranslations;
